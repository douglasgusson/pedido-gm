    private void btAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btAlterarActionPerformed
        int indice = tbAcabamentos.getSelectedRow();
        if (indice == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado");
        } else {
            FrmRegistroAcabamento registroAcabamento
                    = new FrmRegistroAcabamento(this, ((AcabamentoTableModel) tbAcabamentos.getModel()).getColecao(), indice);
            registroAcabamento.setVisible(true);
        }
    }//GEN-LAST:event_btAlterarActionPerformed

    private void btExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btExcluirActionPerformed
        int indice = tbAcabamentos.getSelectedRow();
        if (indice == -1) {
            JOptionPane.showMessageDialog(null, "Nenhum registro selecionado");
        } else {
            Acabamento a = ((AcabamentoTableModel) tbAcabamentos.getModel()).getColecao().get(indice);
            if (GUIUtils.confirmarExclusao(a) == JOptionPane.YES_OPTION) {
                AcabamentoDAO acabamentoDAO = DAOFactory.getDefaultDAOFactory().getAcabamentoDAO();
                acabamentoDAO.excluir(a);
                atualizarTabela();
            }
        }
    }//GEN-LAST:event_btExcluirActionPerformed

